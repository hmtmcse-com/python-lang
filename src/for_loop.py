# For loop
color_names = ["Red", "Green", "Blue", "Yellow"]
for name in color_names:
    print(name)

# For loop else execute when it's finish
color_names = ["Red", "Green", "Blue", "Yellow"]
for name in color_names:
    print(name)
else:
    print("Loop Completed")

print("\n ------ \n")

# For loop else execute when it has no item
color_names = []
for name in color_names:
    print(name)
else:
    print("Empty colors")

