# While loop
start_count = 1
end_count = 6
while start_count <= end_count:
    print(start_count)
    start_count += 1

# ----------------------------------------------------------------------------------------------------------------------
# While loop with else
start_count = 1
end_count = 6
while start_count <= end_count:
    print(start_count)
    start_count += 1
else:
    print("Loop completed")

