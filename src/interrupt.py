# ----------------------------------------------------------------------------------------------------------------------
# Continue
for number in range(1, 11, 1):
    if (number % 2) != 0:
        continue
    print(number)


# ----------------------------------------------------------------------------------------------------------------------
# Break
for number in range(1, 11, 1):
    print(number)
    if number == 5:
        break

# ----------------------------------------------------------------------------------------------------------------------
# Ternary
age = 16
print("Hmm, You are adult.") if age >= 18 else print("You are not adult.")
